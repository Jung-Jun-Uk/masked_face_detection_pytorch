def get_hash(files):
    # Returns a single hash value of a list of files
    return sum(os.path.getsize(f) for f in files if os.path.isfile(f))

    
def create_dataloader(dir_path, path, imgsz, batch_size, stride, opt, hyp=None, 
                      augment=False, cache=False, pad=0.0, rect=False,
                      local_rank=-1, world_size=1):

    # Make sure only the first process in DDP process the dataset first, and the following others can use the cache.
    with torch_distributed_zero_first(local_rank):
        dataset = LoadImagesAndLabels(dir_path, path, imgsz, batch_size,
                                      augment=augment,  # augment images
                                      hyp=hyp,  # augmentation hyperparameters
                                      rect=rect,  # rectangular training
                                      cache_images=cache,
                                      single_cls=opt.single_cls,
                                      stride=int(stride),
                                      pad=pad)
    
    batch_size = min(batch_size, len(dataset))
    num_workers = min([os.cpu_count() // world_size, batch_size if batch_size > 1 else 0, 8])  # number of workers    
    
    train_sampler = torch.utils.data.distributed.DistributedSampler(dataset) if local_rank != -1 else None
    dataloader = torch.utils.data.DataLoader(dataset,
                                             batch_size=batch_size,
                                             num_workers=num_workers,
                                             sampler=train_sampler,
                                             pin_memory=True,
                                             collate_fn=LoadImagesAndLabels.collate_fn)
    return dataloader, dataset


class LoadImagesAndLabels(Dataset): # for training/testing
    def __init__(self, img_dir, anno_path, img_size=640, batch_size=16, augment=False,
                       hyp=None, rect=False, image_weights=False, cache_images=False, 
                       single_cls=False, stride=32, pad=0.0):
                        
        label_dict = {'without_mask': 0, 'with_mask': 1, 'incorrect_mask' : 2}
        # self.img_files, self.labels = self.create_widerface_dataset(img_dir, anno_path, face_label=label_dict['without_mask'])
        self.img_files, self.labels = self.create_pwmfd_dataset(img_dir, label_dict)
        
        num_imgs = len(self.img_files)        
        assert num_imgs > 0, 'No images found in %s. See %s' % (anno_path, help_url)
        
        batch_idx = np.floor(np.arange(num_imgs) / batch_size).astype(np.int)  # batch index                        
        num_batches = batch_idx[-1] + 1  # number of batches

        self.num_imgs = num_imgs # number of images
        self.batch_idx = batch_idx # batch index of image
        self.img_size = img_size
        self.augment = augment
        self.hyp = hyp
        self.image_weights = image_weights
        self.rect = False if image_weights else rect
        self.mosaic = self.augment and not self.rect  # load 4 images at a time into a mosaic (only during training)        
        self.mosaic_border = [-img_size // 2, -img_size // 2]
        
        # Check cache                                                
        cache_path = str(Path(anno_path)) + '.cache'        
        if os.path.isfile(cache_path):            
            cache = torch.load(cache_path)    
            if cache['hash'] != get_hash(self.img_files + [anno_path]):  # dataset changed
                cache = self.cache_labels(cache_path)  # re-cache
        else:
            cache = self.cache_labels()
            cache['hash'] = get_hash(self.img_files + [anno_path])            
            torch.save(cache, cache_path)

        # Get labels
        labels, shapes = zip(*[cache[x] for x in self.img_files])
        self.shapes = np.array(shapes, dtype=np.float64)
        self.labels = list(labels)

        # Rectangular Training  https://github.com/ultralytics/yolov3/issues/232
        if self.rect:
            # Sort by aspect ratio
            shape = self.shapes # [w, h]            
            aspect_ratio = shape[:, 1] / shape[:, 0] # h/w
            irect = aspect_ratio.argsort() 
            self.img_files = [self.img_files[i] for i in irect]
            self.labels = [self.labels[i] for i in irect]
            self.shapes = shape[irect]  # wh
            aspect_ratio = aspect_ratio[irect]

            # Set training image shapes
            shapes = [[1, 1]] * num_batches
            for i in range(num_batches):            
                ari = aspect_ratio[batch_idx == i]
                mini, maxi = ari.min(), ari.max()
                
                if maxi < 1:
                    shapes[i] = [maxi, 1]
                elif mini > 1:
                    shapes[i] = [1, 1 / mini]
            
            self.batch_shapes = np.ceil(np.array(shapes) * img_size / stride + pad).astype(np.int) * stride

        # Cache labels
        num_missing, num_found, num_empty, num_duplicate = 0, 0, 0, 0  # number missing, found, empty, duplicate
        pbar = tqdm(self.img_files)
        for i, file in enumerate(pbar):
            l = self.labels[i]  # label
            if l.shape[0]:                
                assert l.shape[1] == 5, '> 5 label columns: %s' % file
                assert (l >= 0).all(), 'negative labels: %s' % file
                assert (l[:, 1:] <= 1).all(), 'non-normalized or out of bounds coordinate labels: %s' % file
                if np.unique(l, axis=0).shape[0] < l.shape[0]:  # duplicate rows
                    num_duplicate += 1  # print('WARNING: duplicate rows in %s' % self.label_files[i])  # duplicate rows
                if single_cls:
                    l[:, 0] = 0  # force dataset into single-class mode
                self.labels[i] = l
                num_found += 1  # file found
            else:
                num_empty += 1  # print('empty labels for image %s' % self.img_files[i])  # file empty
                # os.system("rm '%s' '%s'" % (self.img_files[i], self.label_files[i]))  # remove
            pbar.desc = 'Scanning labels %s (%g found, %g missing, %g empty, %g duplicate, for %g images)' % (
                cache_path, num_found, num_missing, num_empty, num_duplicate, num_imgs)
        
        if num_found == 0:
            s = 'WARNING: No labels found in %s. See %s' % (os.path.dirname(file) + os.sep, help_url)            
            assert not augment, '%s. Can not train without labels.' % s

        self.imgs = [None] * num_imgs
                
    def __len__(self):
        return len(self.img_files)

    def __getitem__(self, index):
        if self.image_weights:
            index = self.indices[index]

        hyp = self.hyp
        if self.mosaic:
            # Load mosaic
            img, labels = load_mosaic(self, index)
            shapes = None

            # MixUp https://arxiv.org/pdf/1710.09412.pdf
            if random.random() < hyp['mixup']:
                img2, labels2 = load_mosaic(self, random.randint(0, len(self.labels) - 1))
                r = np.random.beta(8.0, 8.0)  # mixup ratio, alpha=beta=8.0
                img = (img * r + img2 * (1 - r)).astype(np.uint8)
                labels = np.concatenate((labels, labels2), 0)

        else:
            # Load image
            img, (h0, w0), (h, w) = load_image(self, index)

            # Letterbox
            shape = self.batch_shapes[self.batch_idx[index]] if self.rect else self.img_size  # final letterboxed shape
            img, ratio, pad = letterbox(img, shape, auto=False, scaleup=self.augment)
            shapes = (h0, w0), ((h / h0, w / w0), pad)  # for COCO mAP rescaling

            # Load labels
            labels = []
            x = self.labels[index]
            if x.size > 0:
                # Normalized xywh to pixel xyxy format
                labels = x.copy()
                labels[:, 1] = ratio[0] * w * (x[:, 1] - x[:, 3] / 2) + pad[0]  # pad width
                labels[:, 2] = ratio[1] * h * (x[:, 2] - x[:, 4] / 2) + pad[1]  # pad height
                labels[:, 3] = ratio[0] * w * (x[:, 1] + x[:, 3] / 2) + pad[0]
                labels[:, 4] = ratio[1] * h * (x[:, 2] + x[:, 4] / 2) + pad[1]

        if self.augment:
            # Augment imagespace
            if not self.mosaic:
                img, labels = random_perspective(img, labels,
                                                 degrees=hyp['degrees'],
                                                 translate=hyp['translate'],
                                                 scale=hyp['scale'],
                                                 shear=hyp['shear'],
                                                 perspective=hyp['perspective'])

            # Augment colorspace
            augment_hsv(img, hgain=hyp['hsv_h'], sgain=hyp['hsv_s'], vgain=hyp['hsv_v'])

            # Apply cutouts
            # if random.random() < 0.9:
            #     labels = cutout(img, labels)

        nL = len(labels)  # number of labels
        if nL:
            labels[:, 1:5] = xyxy2xywh(labels[:, 1:5])  # convert xyxy to xywh
            labels[:, [2, 4]] /= img.shape[0]  # normalized height 0-1
            labels[:, [1, 3]] /= img.shape[1]  # normalized width 0-1

        if self.augment:
            # flip up-down
            if random.random() < hyp['flipud']:
                img = np.flipud(img)
                if nL:
                    labels[:, 2] = 1 - labels[:, 2]

            # flip left-right
            if random.random() < hyp['fliplr']:
                img = np.fliplr(img)
                if nL:
                    labels[:, 1] = 1 - labels[:, 1]

        labels_out = torch.zeros((nL, 6))
        if nL:
            labels_out[:, 1:] = torch.from_numpy(labels)

        # Convert
        img = img[:, :, ::-1].transpose(2, 0, 1)  # BGR to RGB, to 3x416x416
        img = np.ascontiguousarray(img)

        return torch.from_numpy(img), labels_out, self.img_files[index], shapes

    
    def cache_labels(self):
        info_dict = {}
        pbar = tqdm(zip(self.img_files, self.labels), desc='Scanning images', total=len(self.img_files))
        for img_path, labels in pbar:
            img0 = cv2.imread(img_path) # BGR
            height, width, _ = img0.shape            
            shape = (width, height)
            normalize_wh = torch.tensor(img0.shape)[[1, 0, 1, 0]]  # normalization gain whwh            
            # label = []
            labelwithboxes = []
            for label in labels: 
                label = torch.Tensor(label).unsqueeze(0)            
                xyxy = label[0, 1:]            
                xyxy = torch.clamp(xyxy, min=torch.ones_like(xyxy), max=(normalize_wh-1).unsqueeze(0))                
                xywh = xyxy2xywh(xyxy / normalize_wh)
                label[0,1:] = xywh
                labelwithboxes.append(label)
            if len(labelwithboxes) == 0:
                labelwithboxes = np.zeros((0,5), dtype=np.float32)
            else:
                labelwithboxes = torch.cat(labelwithboxes, dim=0).numpy()
            
            info_dict[img_path] = [labelwithboxes, shape]
        
        return info_dict



